#include <stdio.h>
double delta, dx, dy, dz, m[4][5];
int main() {
    for (int i = 1; i <= 3; i++) {
        for (int j = 1; j <= 4; j++) {
            scanf("%lf", &m[i][j]);
        }
    }
    delta = m[1][1] * (m[2][2] * m[3][3] - m[3][2] * m[2][3]) - m[1][2] * (m[2][1] * m[3][3] - m[3][1] * m[2][3]) + m[1][3] * (m[2][1] * m[3][2] - m[3][1] * m[2][2]);
    dx = m[1][4] * (m[2][2] * m[3][3] - m[3][2] * m[2][3]) - m[1][2] * (m[2][4] * m[3][3] - m[3][4] * m[2][3]) + m[1][3] * (m[2][4] * m[3][2] - m[3][4] * m[2][2]);
    dy = m[1][1] * (m[2][4] * m[3][3] - m[3][4] * m[2][3]) - m[1][4] * (m[2][1] * m[3][3] - m[3][1] * m[2][3]) + m[1][3] * (m[2][1] * m[3][4] - m[3][1] * m[2][4]);
    dz = m[1][1] * (m[2][2] * m[3][4] - m[3][2] * m[2][4]) - m[1][2] * (m[2][1] * m[3][4] - m[3][1] * m[2][4]) + m[1][4] * (m[2][1] * m[3][2] - m[3][1] * m[2][2]);
    printf("%.2lf %.2lf %.2lf", dx / delta, dy / delta, dz / delta);
    return 0;
}